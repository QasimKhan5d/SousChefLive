    @pytest.mark.asyncio
    async def test_burnt_food_detection(self):
        """Send burnt food image and ask if it's okay — agent should respond."""
        async with websockets.connect(
            ws_url("vision_burnt"), ssl=ssl_context(),
            open_timeout=15, close_timeout=5,
        ) as ws:
            await ws.send(setup_message())
            await ws.send(image_message("burnt_food.jpg"))
            await ws.send(text_message("Is this looking okay or is it overdone?"))

            events = await collect_events(ws, timeout_s=35)
            transcript = extract_output_transcription(events)
            audio = extract_audio_chunks(events)

